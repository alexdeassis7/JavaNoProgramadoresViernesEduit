package clase3demostracionbuclefor;

public class Clase3DemostracionBucleFor {
  
    public static void main(String[] args) {
        //Estructura FOR |  desde - para
        
        for (int i = 10 ; i >= 0; i--) {
            //cuerpo del for que se ejecuta cuando la condicion es true 
            System.out.println(i);            
            
        }
        
        
                
        
    }
    
}
